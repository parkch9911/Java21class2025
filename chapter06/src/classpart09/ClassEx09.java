package classpart09;

public class ClassEx09 {
	public int sum(int x, int y) {
		return x+y;
	}
	public int minus(int x, int y) {
		return x-y;
	}
	public int mul(int x, int y) {
		return x*y;
	}
	public int dev(int x, int y) {
		return x/y;
	}
	public double mmul(int x, int y) {
		return Math.pow(x, y);
	}
	
}
